print("complex type     :        x = 331j")
print("str type         :        x = '331' ")
print("list type        :        x = ['Kenya', 'Uganda', 'Tanzania'] ")
print("tuple type       :        x = ('Kenya', 'Uganda', 'Tanzania') ")
print("range type       :        x = range(5) ")
print("dict type        :        x = {'country' : 'Kenya', 'currency' : 'Ksh'} ")
print("set type         :        x = {'Man City', 'Arsenal', 'Chealse'} ")
