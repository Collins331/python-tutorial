#There are various data types in python which include:

#Number types: int, float, complex
#Text type: str
#Set types: set, frozenset()
#Mapping type: dict
#Boolean tyes: bool
#Sequence types: lists, tuples, range
#Binary Types: bytes, bytearray, memoryview
#None Type: nonetype

#To determine the the type of data in python, `type()` is used.

print("Examples of Data types include")
print(" ")

print("int type         :        x = 331")
